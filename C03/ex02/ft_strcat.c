char	*ft_strcat(char *dest, char *src)
{
	int	p1;
	int	p2;

	p1 = 0;
	p2 = 0;
	while (dest[p1] != '\0')
	{
		p1++;
	}
	while (src[p2] != '\0')
	{
		dest[p1 + p2] = src[p2];
		p2++;
	}
	dest[p1 + p2] = '\0';
	return (dest);
}
